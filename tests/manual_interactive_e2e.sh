cat > "$REPO_DIR/env.example" <<'EOF'
# Copy these exports into your shell.  Do not commit real secrets.
#
# Claude OAuth is needed for the Claude headless and manual profiles.
# If you do not export it, launch.sh should warn and the dashboard auth
# column should not claim the Claude containers are authenticated.
export CLAUDE_CODE_OAUTH_TOKEN='replace-me'

# Codex ChatGPT auth uses the host auth file.  By default this is:
#   ~/.codex/auth.json
# Set CODEX_AUTH_JSON only if you keep the file somewhere else.
# export CODEX_AUTH_JSON="$HOME/.codex/auth.json"

# Optional fallback if you change Codex profiles to auth=apikey.
# export OPENAI_API_KEY='replace-me'
EOF

cat > "$REPO_DIR/MANUAL_STEPS.md" <<EOF
# Manual Interactive E2E Steps

Run these commands one at a time from this repo:

\`\`\`bash
cd "$REPO_DIR"
export SWARM_CONFIG=swarm.json
\`\`\`

## 0. Credential Preflight

Export Claude OAuth in the shell that will run \`launch.sh\`.  Inline
environment variables only affect the one command they prefix, so use
\`export\` when you plan to run \`start\`, \`shell\`, and \`chat\`
separately.

\`\`\`bash
export CLAUDE_CODE_OAUTH_TOKEN='...'
test -s "\${CODEX_AUTH_JSON:-\$HOME/.codex/auth.json}"
\`\`\`

Expected result:

- The \`test -s\` command exits 0 for Codex ChatGPT auth.
- Do not paste secrets into files in this repo.

## 1. Dashboard Before Launch

\`\`\`bash
./dashboard.sh
\`\`\`

Expected result:

- Numbered rows 1-5 render from \`swarm.json\` as \`configured\`.
- They should show real model, driver, and auth values, not
  \`unknown\` / \`not found\`.
- Omitted \`count\` on \`claude-manual\` and \`count: 0\` on
  \`codex-manual\` should not add numbered rows.
- The post-process row should appear as \`P\`, matching the key that
  starts it.
- The footer should show \`[P] post-process\`.

Press \`q\` to exit.

## 2. Start Headless Agents With Dashboard

\`\`\`bash
./launch.sh start --dashboard
\`\`\`

Expected result:

- Five numbered containers start: 3 Claude OAuth and 2 Codex ChatGPT.
- The dashboard header reports 5 agents.
- Press \`1\` through \`5\` to inspect numbered-agent logs.
- \`p\` should only tail post-process logs after a post-process
  container exists.
- \`P\` should ask for confirmation before starting post-process.

This fixture uses \`max_idle: 3\` and normal prompts, so agents can
finish naturally.  Once you have seen enough, press \`s\` in the
dashboard or run:

\`\`\`bash
./launch.sh stop
\`\`\`

## 3. Codex Manual Shell

\`\`\`bash
./launch.sh shell --agent codex-manual
\`\`\`

Inside the container, run:

\`\`\`bash
test -s ~/.codex/auth.json && echo "codex auth mounted"
codex --version
cat prompts/manual.md
mkdir -p test-results
{
  printf 'profile=%s\\n' "\${SWARM_INTERACTIVE_PROFILE}"
  printf 'branch=%s\\n' "\${SWARM_INTERACTIVE_BRANCH}"
  printf 'driver=%s\\n' "\${SWARM_DRIVER}"
  printf 'model=%s\\n' "\${SWARM_MODEL}"
  date -u '+time=%Y-%m-%dT%H:%M:%SZ'
} > "test-results/manual-\${SWARM_INTERACTIVE_PROFILE}.md"
git add test-results
git commit -m "Record Codex manual smoke"
exit
\`\`\`

Expected result:

- The auth.json check prints \`codex auth mounted\`.
- Exiting pushes a \`swarm/*/interactive-codex-manual-*\` branch.

## 4. Claude Manual Shell

\`\`\`bash
./launch.sh shell --agent claude-manual
\`\`\`

Inside the container, run:

\`\`\`bash
claude auth status
claude --version
cat prompts/manual.md
mkdir -p test-results
{
  printf 'profile=%s\\n' "\${SWARM_INTERACTIVE_PROFILE}"
  printf 'branch=%s\\n' "\${SWARM_INTERACTIVE_BRANCH}"
  printf 'driver=%s\\n' "\${SWARM_DRIVER}"
  printf 'model=%s\\n' "\${SWARM_MODEL}"
  date -u '+time=%Y-%m-%dT%H:%M:%SZ'
} > "test-results/manual-\${SWARM_INTERACTIVE_PROFILE}.md"
git add test-results
git commit -m "Record Claude manual smoke"
exit
\`\`\`

Expected result:

- \`claude auth status\` reports the injected OAuth credential.
- Exiting pushes a \`swarm/*/interactive-claude-manual-*\` branch.

## 5. Native Interactive UIs

Use these only when you want to test the actual driver UI, not just the
container shell:

\`\`\`bash
./launch.sh chat --agent codex-manual
./launch.sh chat --agent claude-manual
\`\`\`

When the UI opens, paste the contents of \`prompts/manual.md\`.  Ask
the agent to make and commit one file under \`test-results/\`, then
exit the UI.  The container exit hook should push the interactive
branch.

## 6. Dashboard Interactive Rows

\`\`\`bash
./dashboard.sh
\`\`\`

Expected result:

- Interactive containers appear as \`I1\`, \`I2\`, and so on.
- A committed but unharvested interactive branch should show
  \`unharvested\`.
- A running shell with uncommitted changes should show \`dirty\`.
- After harvest, the row should show \`harvested\`.

## 7. Post-Process From Dashboard

\`\`\`bash
./dashboard.sh
\`\`\`

Press \`P\`, confirm with \`y\`, and watch the \`P\` row.  After the
post-process container exists, lowercase \`p\` should tail its logs.

Equivalent non-dashboard command:

\`\`\`bash
./launch.sh post-process
\`\`\`

Expected result:

- Post-process uses the Codex ChatGPT profile from \`post_process\`.
- It writes \`test-results/post-process-summary.md\`.
- The summary ends with \`POST_PROCESS_DONE\`.

## 8. Harvest And Inspect

\`\`\`bash
./harvest.sh --dry
./harvest.sh
git log --oneline --max-count=16
find test-results -maxdepth 1 -type f -print | sort
\`\`\`

Expected result:

- Headless agent commits, interactive commits, and post-process output
  are all merge candidates.
- Harvest merges \`agent-work\` plus \`swarm/*/interactive-*\`
  branches into the current branch.

## Cleanup

\`\`\`bash
./launch.sh stop
\`\`\`

Optional cleanup for this disposable repo's Docker artifacts:

\`\`\`bash
docker ps -a --format '{{.Names}}' | grep "\$(basename "\$PWD")-agent" || true
\`\`\`

Remove the temp directory when you are done:

\`\`\`bash
rm -rf "$REPO_DIR"
\`\`\`
EOF

cat > "$REPO_DIR/manual-workflow.sh" <<'EOF'
#!/bin/bash
set -euo pipefail

# This helper prints the manual runbook path and exits.
#
# Keep it as a reminder that the workflow is intentionally manual:
# launching these agents can spend real API tokens, and the point is
# to inspect dashboard states, auth behavior, interactive branches, and
# post-process behavior with a human in the loop.

printf 'Read MANUAL_STEPS.md, then run the commands one at a time.\n'
printf 'Suggested first command:\n\n'
printf '  export SWARM_CONFIG=swarm.json\n'
printf '  ./dashboard.sh\n'
EOF
chmod +x "$REPO_DIR/manual-workflow.sh"

cd "$REPO_DIR"
if git init -b main >/dev/null 2>&1; then
    :
else
    git init >/dev/null
    git checkout -b main >/dev/null 2>&1 || true
fi
git config user.name "Manual Swarm Tester"
git config user.email "manual-swarm@example.invalid"
git add .
git commit -m "Create manual swarm smoke fixture" >/dev/null

cat <<EOF
Created manual interactive E2E repo:

  $REPO_DIR

Next commands:

  cd "$REPO_DIR"
  export SWARM_CONFIG=swarm.json
  ./dashboard.sh

Read the full manual runbook:

  $REPO_DIR/MANUAL_STEPS.md
EOF
