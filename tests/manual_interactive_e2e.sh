#!/bin/bash
set -euo pipefail

# Manual interactive E2E fixture generator.
#
# This is intentionally not named test_*.sh, so tests/test.sh --unit
# will not discover it.  Running this script creates a throwaway git
# repo with a swarm.json that exercises the manual scenarios we have
# been testing by hand:
#
# - numbered headless Claude and Codex agents;
# - an omitted-count Claude profile for interactive-only use;
# - a count: 0 Codex profile for interactive-only use;
# - Codex ChatGPT auth.json mounting;
# - Claude OAuth env forwarding;
# - dashboard configured rows before containers exist;
# - dashboard I* rows for interactive containers;
# - dashboard P row and [P] post-process action.
#
# The generated config uses normal prompts and max_idle=3.  It does
# not include a timeboxed "keep running for N minutes" prompt.  Stop
# agents manually once the scenario has been observed, or let the
# short idle threshold finish the run naturally.

usage() {
    cat <<'HELP'
Usage: tests/manual_interactive_e2e.sh [TARGET_DIR]

Create a throwaway repo for manually exercising interactive swarms.

When TARGET_DIR is omitted, a unique repo is created under /tmp.
When TARGET_DIR is provided, it must be empty or not exist yet.

The script does not launch agents.  It writes MANUAL_STEPS.md in the
new repo with the command sequence to run by hand.
HELP
}

if [ "${1:-}" = "-h" ] || [ "${1:-}" = "--help" ]; then
    usage
    exit 0
fi

TESTS_DIR="$(cd "$(dirname "$0")" && pwd)"
SWARM_DIR="$(cd "$TESTS_DIR/.." && pwd)"

if [ -n "${1:-}" ]; then
    REPO_DIR="$1"
    if [ -e "$REPO_DIR" ] \
            && [ -n "$(find "$REPO_DIR" -mindepth 1 -maxdepth 1 \
                -print -quit 2>/dev/null)" ]; then
        echo "ERROR: TARGET_DIR exists and is not empty: $REPO_DIR" >&2
        exit 1
    fi
    mkdir -p "$REPO_DIR"
else
    PARENT_DIR="$(mktemp -d /tmp/claude-swarm-manual.XXXXXX)"
    SUFFIX="${PARENT_DIR##*.}"
    REPO_DIR="${PARENT_DIR}/manual-swarm-${SUFFIX}"
    mkdir -p "$REPO_DIR"
fi

REPO_DIR="$(cd "$REPO_DIR" && pwd)"

write_wrapper() {
    local output="$1" target="$2"
    {
        printf '#!/bin/bash\n'
        printf 'set -euo pipefail\n'
        printf 'exec %q "$@"\n' "$target"
    } > "$output"
    chmod +x "$output"
}

mkdir -p \
    "$REPO_DIR/.claude" \
    "$REPO_DIR/prompts" \
    "$REPO_DIR/scripts" \
    "$REPO_DIR/test-results"

write_wrapper "$REPO_DIR/launch.sh" "$SWARM_DIR/launch.sh"
write_wrapper "$REPO_DIR/dashboard.sh" "$SWARM_DIR/dashboard.sh"
write_wrapper "$REPO_DIR/harvest.sh" "$SWARM_DIR/harvest.sh"

cat > "$REPO_DIR/README.md" <<'EOF'
# claude-swarm manual interactive fixture

This repository is generated by `tests/manual_interactive_e2e.sh`.
It is a disposable target for manually exercising headless agents,
interactive containers, auth forwarding, dashboard rendering, harvest,
and post-process behavior.

The generated prompts are intentionally normal task prompts.  They do
not ask agents to wait for a timer.  The swarm config uses
`max_idle: 3` so the run can finish naturally; stop the
run manually when you have observed the behavior you need.
EOF

cat > "$REPO_DIR/AGENTS.md" <<'EOF'
# Manual Smoke Instructions

This repo is disposable.  Keep changes small, commit them, and avoid
network access except for the model CLI call itself.

For headless smoke work, write only under `test-results/`.  For manual
interactive work, create one obvious file under `test-results/`, commit
it, and exit the shell or native UI so the branch is pushed.
EOF

cat > "$REPO_DIR/.claude/CLAUDE.md" <<'EOF'
# Claude Context

This is a manual claude-swarm smoke fixture.  Prefer direct shell
commands, keep commits small, and write observable evidence under
`test-results/`.
EOF

cat > "$REPO_DIR/scripts/setup.sh" <<'EOF'
#!/bin/bash
set -euo pipefail

mkdir -p test-results
{
    printf 'setup ran at %s\n' "$(date -u +%Y-%m-%dT%H:%M:%SZ)"
    printf 'agent=%s driver=%s model=%s\n' \
        "${AGENT_ID:-unknown}" \
        "${SWARM_DRIVER:-unknown}" \
        "${SWARM_MODEL:-unknown}"
} >> test-results/setup.log
EOF
chmod +x "$REPO_DIR/scripts/setup.sh"

cat > "$REPO_DIR/prompts/headless.md" <<'EOF'
You are running a manual claude-swarm smoke test in a disposable repo.

Make one small, unique commit that proves this numbered agent ran:

1. Inspect `git status --short` and `README.md`.
2. Create `test-results/headless-${AGENT_ID:-unknown}.md` if it does
   not already exist.  Use a shell command so `${AGENT_ID}` expands in
   the container.
3. Include the agent id, driver, model, UTC time, and the first line of
   `git rev-parse --short HEAD`.
4. Commit the file with a concise message.

If your marker file already exists, make no changes and exit normally.
Do not sleep, wait for a timer, or run an infinite loop.
EOF

cat > "$REPO_DIR/prompts/manual.md" <<'EOF'
Manual interactive smoke prompt.

Verify the active driver and auth, then make one small commit:

1. For Claude Code, run `claude auth status` and `claude --version`.
2. For Codex, run `test -s ~/.codex/auth.json` and `codex --version`.
3. Create or append `test-results/manual-${SWARM_INTERACTIVE_PROFILE}.md`.
4. Include the branch, profile, driver, model, and UTC time.
5. Commit the file and exit the shell or native UI.

Use shell commands so the environment variables expand inside the
container.  Do not leave uncommitted changes unless the scenario is
specifically testing the dashboard `dirty` state.
EOF

cat > "$REPO_DIR/prompts/post-process.md" <<'EOF'
You are the post-process agent for a manual claude-swarm smoke test.

Summarize the current repository state into
`test-results/post-process-summary.md`:

1. List files in `test-results/`.
2. Summarize recent commits from `git log --oneline --max-count=12`.
3. Note whether `agent_logs/interactive_state` exists.
4. End with `POST_PROCESS_DONE`.
5. Commit the summary.

If the summary is already current, make no changes and exit normally.
Do not sleep, wait for a timer, or run an infinite loop.
EOF

cat > "$REPO_DIR/swarm.json" <<'EOF'
